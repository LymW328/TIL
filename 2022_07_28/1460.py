class Collaz():
    cnt = 0
    def __init__(self, num):
        self.num = num
        cnt += 1


    def edddiv(self, num):
        if self.num % 2 == 0:
            return (self.num //2) + 1
        else:
            pass
        
    def odddiv(self, num):
        if self.num % 2 != 0:
            return self.num //3
        else:
            pass
         
        